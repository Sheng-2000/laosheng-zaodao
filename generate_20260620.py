# 自动设置 market-block 顶部边框颜色（按块内 up/down 多数方向）
def fix_market_block_borders(html):
    block_pattern = r'(<div class="market-block" style="border-top:2px solid rgba\(255,255,255,0\.12\);">)(.*?)(</div>)'
    def choose_border(m):
        open_tag, body, close_tag = m.group(1), m.group(2), m.group(3)
        ups = len(re.findall(r'class="market-val up"', body))
        downs = len(re.findall(r'class="market-val down"', body))
        if ups > downs:
            color = '#f85149'
        elif downs > ups:
            color = '#3fb950'
        else:
            color = '#f0b429'
        new_open = open_tag.replace('rgba(255,255,255,0.12)', color)
        return new_open + body + close_tag
    return re.sub(block_pattern, choose_border, html, flags=re.S)

