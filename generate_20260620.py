# 上涨关键词：+、涨、创新高、流入；下跌：-、跌、创新低、净卖出/流出、下调/贬值
def classify(text):
    t = text.lower()
    if '休市' in t or '待更新' in t or '--' in t:
        return 'neutral'
    if any(w in t for w in ['创新低', '净卖出', '净流出', '下调', '贬值', '流出']):
        return 'down'
    if '跌多涨少' in t or '跌多于涨' in t:
        return 'down'
    if any(w in t for w in ['+', '涨', '高', '流入', '上升', '上调']):
        return 'up'
    if any(w in t for w in ['-', '跌', '下降']):
        return 'down'
    return 'neutral'

# 修正 market-val 的 class（覆盖所有 market-val neutral）
def fix_market_val_class(html):
    pattern = r'(<div class="market-row">.*?<span class="market-val neutral"(?:\s+style="[^"]*")?>.*?</span></div>)'
    for block in re.findall(pattern, html, re.S):
        text = re.sub(r'<[^>]+>', '', block)
        cls = classify(text)
        new_block = re.sub(r'class="market-val neutral"', f'class="market-val {cls}"', block, count=1)
        html = html.replace(block, new_block)
    return html

