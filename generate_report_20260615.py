    # 读取模板
    if not TEMPLATE_PATH.exists():
        print(f"❌ 模板文件不存在: {TEMPLATE_PATH}")
        sys.exit(1)

    with open(TEMPLATE_PATH, "r", encoding="utf-8") as f:
        template_content = f.read()

    # 统计原始占位符数量
    import re as _re
    placeholders = set(_re.findall(r"\{\{([^{}]+)\}\}", template_content))
    total_placeholders = len(placeholders)
    print(f"📋 模板中共有 {total_placeholders} 个占位符")
    print(f"📝 replacements 字典中共有 {len(replacements)} 个映射")

    # 批量替换
    output_content = template_content
    replaced_count = 0
    missing_keys = []

    for key, value in replacements.items():
        placeholder = "{{" + key + "}}"
        if placeholder in output_content:
            output_content = output_content.replace(placeholder, str(value))
            replaced_count += 1
        else:
            missing_keys.append(key)

    # 检查未替换的占位符
    remaining = set(_re.findall(r"\{\{([^{}]+)\}\}", output_content))
    if remaining:
        print(f"\n⚠️  仍有 {len(remaining)} 个占位符未替换:")
        for k in sorted(remaining):
            print(f"   - {k}")

    if missing_keys:
        print(f"\nℹ️  replacements 中有 {len(missing_keys)} 个键在模板中未找到（可能是预留项）:")
        for k in sorted(missing_keys)[:20]:
            print(f"   - {k}")
        if len(missing_keys) > 20:
            print(f"   ... 还有 {len(missing_keys) - 20} 个")

    # 写入输出
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        f.write(output_content)

    print(f"\n✅ 成功替换 {replaced_count} 个占位符")
